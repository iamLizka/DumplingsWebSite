document.getElementById('btn-1').addEventListener('click', function() {
var button = this;
var imageUrl = 'static/images/avatar_none.png'; // Укажите здесь ссылку на изображение кнопки
var imgElement = document.getElementById('avatarka');
imgElement.src = imageUrl;
// Получаем элемент input
var myInputElement = document.getElementById("avatar-user");
// Устанавливаем значение переменной в поле value элемента input
myInputElement.value = imageUrl;
});

